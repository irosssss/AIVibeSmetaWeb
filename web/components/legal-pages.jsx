/* локальный рендер markdown-lite: как ArticleBody журнала + поддержка «- » списков
   (ArticleBody не в window — копируем паттерн, не тянем зависимость, см. гочу window-глобалов) */
function LegalBody({ text }) {
  const blocks = String(text || "").split(/\n\n+/).map((b) => b.trim()).filter(Boolean);
  return (
    <div style={{ marginTop: 30, display: "flex", flexDirection: "column", gap: 18 }}>
      {blocks.map((b, i) => {
        if (b.startsWith("## ")) return <h2 key={i} className="display" style={{ fontSize: "var(--fs-21)", marginTop: 14 }}>{b.slice(3)}</h2>;
        if (b.split("\n").every((l) => l.startsWith("- "))) return (
          <ul key={i} style={{ paddingLeft: 22, display: "flex", flexDirection: "column", gap: 8 }}>
            {b.split("\n").map((l, j) => <li key={j} style={{ fontSize: "var(--fs-15)", lineHeight: 1.7, color: "var(--text)" }}>{l.slice(2)}</li>)}
          </ul>
        );
        return <p key={i} style={{ fontSize: "var(--fs-15)", lineHeight: 1.7, color: "var(--text)" }}>{b}</p>;
      })}
    </div>
  );
}

